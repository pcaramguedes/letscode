// var, let, const

let escola = "Lets"; // string
console.log("escola", escola, typeof escola);

const fundacao = 2020; // number
console.log("fundacao", fundacao, typeof fundacao);

const tecnologia = true; // boolean
console.log("tecnologia", tecnologia, typeof tecnologia); 

let nula = null; // null
console.log("nula", nula, typeof nula);

let indefinida; // undefined
console.log("indefinida", indefinida, typeof indefinida);

